def area_quadrado(l):
    return l**2

def area_triangulo(b,a):
    return (b*a)/2

area_quadrado_1 = area_quadrado(4)
area_quadrado_2 = area_quadrado(9)

print(area_quadrado_1)
print(area_quadrado_2)

print("\n")

area_triangulo_1 = area_triangulo(6,9)
area_triangulo_2 = area_triangulo(5,8)
area_triangulo_3 = area_triangulo(5,2)

print(area_triangulo_1)
print(area_triangulo_2)
print(area_triangulo_3)
