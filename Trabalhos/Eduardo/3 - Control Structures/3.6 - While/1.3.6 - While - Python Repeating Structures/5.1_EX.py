# Uma das estruturas de repetição do Python é o While, que repete um bloco enquanto a condição
# for verdadeira. Seu formato é apresentado onde a condição é uma expressão lógica, e  bloco representa
# as linhas de programa a repetir enquanto o resultado da condição for verdadeiro

x = 1

while x <= 100:
    print(x)
    x = x + 1